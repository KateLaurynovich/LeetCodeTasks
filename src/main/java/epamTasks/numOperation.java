package epamTasks;

public class numOperation {
    public static void main(String[] args) {
        int number = 0;
        do {
            System.out.println(number);
            number++;
        } while (number < 10);

        for (int i = 0; i < 10; i++) {
            System.out.println("i = " + i);
        }
    }
}
